<script>
    const selectId = 'select-country-{{ $this->getId() }}';
    const el = document.getElementById(selectId);
    let tomSelectInstance = null;

    if (el && !el.tomselect) {
        tomSelectInstance = new TomSelect(el, {
            copyClassesToDropdown: false,
            dropdownParent: 'body',
            controlInput: '<input>',
            render: {
                item: function (data, escape) {
                    if (data.customProperties) {
                        return '<div><span class="dropdown-item-indicator">' + data.customProperties + '</span>' + escape(data.text) + '</div>';
                    }
                    return '<div>' + escape(data.text) + '</div>';
                },
                option: function (data, escape) {
                    if (data.customProperties) {
                        return '<div><span class="dropdown-item-indicator">' + data.customProperties + '</span>' + escape(data.text) + '</div>';
                    }
                    return '<div>' + escape(data.text) + '</div>';
                },
            },
        });

        // Set initial value if the Livewire property already has a value (e.g., edit form)
        const initialValue = $wire.country;
        if (initialValue) {
            tomSelectInstance.setValue(initialValue, true);
        }

        tomSelectInstance.on('change', (values) => {
            $wire.$set('country', values);
        });
    }
</script>
@endscript